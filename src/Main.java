public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int sum = a + b;
        int difference = a - b;
        int product = a * b;

        System.out.println("Bilangan pertama (a): " + a);
        System.out.println("Bilangan kedua (b): " + b);
        System.out.println("----------------------------------");
        System.out.println("Hasil Penjumlahan (a + b): " + sum);
        System.out.println("Hasil Pengurangan (a - b): " + difference);
        System.out.println("Hasil Perkalian  (a * b): " + product);
    }
}
